#include<iostream>
using namespace std;
int main(){
    //INCREMENT
    int x = 13;
    x++; //Post-Increment
    ++x; //Pre-Increment
    cout<<x<<endl;
    //DECREMENT
    int y = 24;
    y--; //Post-Decrement
    --y; //Pre-Decrement
    cout<<y<<endl;

    float a = 9;
    float b = 7;
    cout<<a/b<<endl;

    bool x1 = true;
    bool x2 = false;
    cout<<x1<<endl;
    cout<<x2<<endl;
}